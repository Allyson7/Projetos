    private void jBEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEntrarActionPerformed
        String login = jTFLogin.getText();

        UsuarioDao ud = new UsuarioDao();
        UsuarioBean usuario = ud.logar(login);
        if (usuario == null) {
            JOptionPane.showMessageDialog(rootPane, "Login Invalido");
        } else {
            if (usuario.getSenha().equals(FrameWork.criptografar(jPFSenha.getText()))) {
                new FormPrincipal().setVisible(true);
                dispose();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Senha Invalida");
            }
        }
    }//GEN-LAST:event_jBEntrarActionPerformed

